    private void bComprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bComprobarActionPerformed
        try{
            String nEvento = tfNEvento.getText();
            validarNombre(nEvento);
            boolean encontrado = Ejercicio2BD4o.comprobar(nEvento);
            
            if(encontrado==true){
                tfDni.setEnabled(true);
                tfNombre.setEnabled(true);
                tfApellidos.setEnabled(true);
                tfTelefono.setEnabled(true);
                tfEmpresa.setEnabled(true);
                bApuntarse.setEnabled(true);
            }
        }
        catch(sinTexto e){
            JOptionPane.showMessageDialog(null, "Hay que introducir el nombre del evento");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,"Problemas al modificar " + e.getMessage());
        }
    }//GEN-LAST:event_bComprobarActionPerformed

