    private void bApuntarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bApuntarseActionPerformed
        try{
            if (tfDni.getText().isEmpty())
            throw new sinTexto();
            if (tfNombre.getText().isEmpty())
            throw new sinTexto();
            if (tfApellidos.getText().isEmpty())
            throw new sinTexto();
            if (tfTelefono.getText().isEmpty())
            throw new sinTexto();
            if (tfEmpresa.getText().isEmpty())
            throw new sinTexto();
            
            Ejercicio2BD4o.apuntarPersona(tfNombre.getText(),tfDni.getText(),tfApellidos.getText(),tfTelefono.getText(),tfEmpresa.getText()); 
        }
        catch(sinTexto e){
            JOptionPane.showMessageDialog(null, "Algunas casillas no estan rellenadas");
        }
        catch(Exception e){
             JOptionPane.showMessageDialog(null, e.getClass());
        }
    }//GEN-LAST:event_bApuntarseActionPerformed

    public static void validarNombre(String nombre)throws Exception{
        
        if(nombre.isEmpty())
            throw new sinTexto();
    }
